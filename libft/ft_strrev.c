#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	temp;

	i = 0;
	j = 0;
	while (s[i])
		i++;
	while (j < i)
	{
		temp = s[j];
		s[j] = s[i - 1];
		s[i - 1] = temp;
		j++;
		i--;
	}
	return (s);
}
