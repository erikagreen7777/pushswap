#include "libft.h"

int		ft_ispunct(int c)
{
	if ((c >= 33 && c <= 47) && (c >= 58 && c <= 63))
		return (1);
	if ((c >= 91 && c <= 95) && (c >= 123 && c <= 126))
		return (1);
	else
		return (0);
}
